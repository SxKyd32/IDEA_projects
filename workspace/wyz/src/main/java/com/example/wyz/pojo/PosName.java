package com.example.wyz.pojo;

import javax.persistence.*;

@Entity
@Table(name = "pos_name", schema = "wyz", catalog = "")
public class PosName {
    private int id;
    private String name;

    @Id
    @Column(name = "id")
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Basic
    @Column(name = "name")
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        PosName posName = (PosName) o;

        if (id != posName.id) return false;
        if (name != null ? !name.equals(posName.name) : posName.name != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        return result;
    }
}
